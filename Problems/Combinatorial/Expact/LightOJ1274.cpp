#include <bits/stdc++.h>

int main()
{
    for(int T,n,b,_=scanf("%d",&T);T--;++_)
    {
        scanf("%d %d",&n,&b);
        int p=3*n-b,q=n-p;
        printf("Case %d: %8lf\n",_,n-(p*(p-1.0)/n+q*(q-1.0)/n+1.0*q/n));
    }
    return 0;
}

